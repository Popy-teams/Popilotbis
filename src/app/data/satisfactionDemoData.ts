function r(
  partial: SurveyResponse & { date?: string; respondent?: string }
): SurveyResponse {
  return {
    ...partial,
    submittedAt: partial.submittedAt ?? partial.date ?? '2026-01-10',
    respondentName: partial.respondentName ?? partial.respondent,
    answers: partial.answers ?? {},
    keyTopics: partial.keyTopics ?? [],
    sentiment: partial.sentiment ?? 'neutral',
    status: partial.status ?? 'new',
  };
}

